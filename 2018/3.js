function visualisation(){
	tooltip = document.createElement("div");
	tooltip.id = "tooltip";
	document.body.appendChild(tooltip);

	for(i=0;i<claims.length;i++){
		var ele = document.createElement("div");
		ele.id = "box_"+parseInt(i+1);
		ele.className = "box";

		ele.style.left = claims[i]["start_pos"][0]+"px";
		ele.style.top = claims[i]["start_pos"][1]+"px";

		ele.style.width = claims[i]["size"][0]+"px";
		ele.style.height = claims[i]["size"][1]+"px";

		ele.addEventListener("mouseover",function(e){
			var id = e.target.id.split("_")[1];
			var area = claims[id]["size"][0]*claims[id]["size"][1];

			tooltip.style.top = e.y+"px";
			tooltip.style.left = e.x+"px";
			tooltip.innerHTML = "<b>ID:</b> "+id+"<br><b>Area:</b> "+area+"<br>";
		});

		document.body.appendChild(ele);
	}

	fab = document.createElement("div");
	fab.id = "fabric";
	document.body.appendChild(fab);

}

